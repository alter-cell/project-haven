const STORAGE_KEY = "petal-pages-progress";
let positions = {};
let activeBookId = null;
let chapterIndex = 0;
let pageIndex = 0;
let lastFocusedElement = null;

try {
  positions = JSON.parse(localStorage.getItem(STORAGE_KEY)) || {};
} catch {
  positions = {};
}

function savePosition() {
  positions[activeBookId] = { chapter: chapterIndex, page: pageIndex };
  try {
    localStorage.setItem(STORAGE_KEY, JSON.stringify(positions));
  } catch {
    // Reading still works when storage is unavailable (for example, private mode).
  }
}

function getPosition(bookId) {
  const saved = positions[bookId] || {};
  const book = library[bookId];
  const chapter = Math.min(Math.max(Number(saved.chapter) || 0, 0), book.chapters.length - 1);
  const page = Math.min(Math.max(Number(saved.page) || 0, 0), book.chapters[chapter].pages.length - 1);
  return { chapter, page };
}

function showFeedback(message) {
  elements.feedback.textContent = message;
  clearTimeout(showFeedback.timer);
  showFeedback.timer = setTimeout(() => { elements.feedback.textContent = ""; }, 2600);
}

function showDetails() {
  elements.detail.classList.remove("hidden");
  elements.reader.classList.add("hidden");
  elements.reader.setAttribute("aria-hidden", "true");
  elements.back.textContent = "← Back to shelf";
  elements.start.focus();
}

function renderChapterList(book) {
  elements.chapters.replaceChildren();
  const saved = getPosition(activeBookId);

  book.chapters.forEach((chapter, index) => {
    const button = document.createElement("button");
    button.type = "button";
    button.className = "chapter-item";
    button.textContent = `${index + 1}. ${chapter.title} · ${chapter.pages.length} pages`;
    if (index === saved.chapter) button.classList.add("chapter-current");
    button.addEventListener("click", () => openChapter(index, 0));
    elements.chapters.append(button);
  });
}

function openBook(bookId) {
  const book = library[bookId];
  if (!book) return;
  activeBookId = bookId;
  lastFocusedElement = document.activeElement;
  const saved = getPosition(bookId);
  elements.title.textContent = book.title;
  elements.author.textContent = book.author;
  elements.synopsis.textContent = book.synopsis;
  elements.cover.className = `detail-cover ${book.coverClass}`;
  elements.resume.textContent = saved.chapter || saved.page
    ? `Saved at chapter ${saved.chapter + 1}, page ${saved.page + 1}`
    : "Not started";
  elements.start.textContent = saved.chapter || saved.page ? "Continue Reading" : "Start Reading";
  renderChapterList(book);
  elements.detail.classList.remove("hidden");
  elements.reader.classList.add("hidden");
  elements.reader.setAttribute("aria-hidden", "true");
  elements.back.textContent = "← Back to shelf";
  elements.overlay.classList.add("open");
  elements.overlay.setAttribute("aria-hidden", "false");
  document.body.classList.add("reader-open");
  elements.card.scrollTop = 0;
  setTimeout(() => elements.start.focus(), 350);
}

function renderPage() {
  const book = library[activeBookId];
  const chapter = book.chapters[chapterIndex];
  const pagesBefore = book.chapters.slice(0, chapterIndex).reduce((sum, item) => sum + item.pages.length, 0);
  const totalPages = book.chapters.reduce((sum, item) => sum + item.pages.length, 0);
  const currentBookPage = pagesBefore + pageIndex + 1;

  elements.chapterTitle.textContent = chapter.title;
  elements.pageCounter.textContent = `Page ${pageIndex + 1} of ${chapter.pages.length}`;
  elements.progress.max = totalPages;
  elements.progress.value = currentBookPage;
  elements.progress.setAttribute("aria-label", `${Math.round(currentBookPage / totalPages * 100)}% of book read`);
  elements.text.textContent = chapter.pages[pageIndex];
  elements.text.classList.remove("page-enter");
  void elements.text.offsetWidth;
  elements.text.classList.add("page-enter");
  elements.previous.disabled = chapterIndex === 0 && pageIndex === 0;
  elements.next.textContent = currentBookPage === totalPages ? "Finish book" : "Next page";
  savePosition();
}

function openChapter(nextChapter, nextPage = 0) {
  chapterIndex = nextChapter;
  pageIndex = nextPage;
  elements.detail.classList.add("hidden");
  elements.reader.classList.remove("hidden");
  elements.reader.setAttribute("aria-hidden", "false");
  elements.back.textContent = "← Book details";
  elements.card.scrollTop = 0;
  renderPage();
  elements.text.focus({ preventScroll: true });
}

function closeBook() {
  elements.overlay.classList.remove("open");
  elements.overlay.setAttribute("aria-hidden", "true");
  document.body.classList.remove("reader-open");
  setTimeout(() => lastFocusedElement?.focus(), 300);
}

function changePage(direction) {
  const book = library[activeBookId];
  const chapter = book.chapters[chapterIndex];

  if (direction > 0 && pageIndex < chapter.pages.length - 1) pageIndex++;
  else if (direction > 0 && chapterIndex < book.chapters.length - 1) {
    chapterIndex++;
    pageIndex = 0;
  } else if (direction < 0 && pageIndex > 0) pageIndex--;
  else if (direction < 0 && chapterIndex > 0) {
    chapterIndex--;
    pageIndex = book.chapters[chapterIndex].pages.length - 1;
  } else if (direction > 0) {
    showFeedback(`You finished “${book.title}”.`);
    showDetails();
    renderChapterList(book);
    return;
  } else return;

  renderPage();
}

function runSearch() {
  const query = elements.search.value.trim().toLowerCase();
  let matches = 0;
  elements.books.forEach((button) => {
    const book = library[button.dataset.bookId];
    const isMatch = !query || `${book.title} ${book.author}`.toLowerCase().includes(query);
    button.hidden = !isMatch;
    if (isMatch) matches++;
  });
  showFeedback(query
    ? matches ? `${matches} ${matches === 1 ? "book" : "books"} found.` : `No books found for “${elements.search.value.trim()}”.`
    : "Showing the full library.");
}

elements.books.forEach((button) => button.addEventListener("click", () => openBook(button.dataset.bookId)));
elements.start.addEventListener("click", () => {
  const saved = getPosition(activeBookId);
  openChapter(saved.chapter, saved.page);
});
elements.back.addEventListener("click", () => {
  if (!elements.reader.classList.contains("hidden")) showDetails();
  else closeBook();
});
elements.previous.addEventListener("click", () => changePage(-1));
elements.next.addEventListener("click", () => changePage(1));
elements.searchButton.addEventListener("click", runSearch);
elements.search.addEventListener("input", runSearch);
elements.search.addEventListener("keydown", (event) => {
  if (event.key === "Enter") runSearch();
});
elements.overlay.addEventListener("click", (event) => {
  if (event.target === elements.overlay) closeBook();
});
document.addEventListener("keydown", (event) => {
  if (!elements.overlay.classList.contains("open")) return;
  if (event.key === "Escape") {
    if (!elements.reader.classList.contains("hidden")) showDetails();
    else closeBook();
  }
  if (!elements.reader.classList.contains("hidden") && event.key === "ArrowRight") changePage(1);
  if (!elements.reader.classList.contains("hidden") && event.key === "ArrowLeft") changePage(-1);
});

function updateSearchPill() {
  elements.searchCard.classList.toggle("pill-mode", window.scrollY > 120 && !elements.overlay.classList.contains("open"));
}

window.addEventListener("scroll", updateSearchPill, { passive: true });
